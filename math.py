# Built-in Math Functions
# min() and max()
x=min(5,10,15)
y=max(5,10,15)
print(x)
print(y)


# abs() function returns the absolute (positive) value of the specified number
x=abs(-7.25)
print(x)

# pow(x, y) function returns the value of x to the power of y.
x=pow(4,3)
print(x)

# math.sqrt() method for example, returns the square root of a number:
import math
x=math.sqrt(64)
print(x)

# math.ceil() method rounds a number upwards to its nearest integer,
x=math.ceil(1.4)
print(x)
# math.floor() method rounds a number downwards to its nearest integer
y=math.floor(1.4)
print(y)
# math.pi constant, returns the value of PI (3.14...):
x=math.pi
print(x)
